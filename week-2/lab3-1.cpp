#include <iostream>

int main() {
    int num = 10;
    std::cout << "num = " << num << '\n';

    //arthemitic example
    num = num + 1;
    std::cout << "num + 1= " << num << '\n';

    //floating point example
    float num2 = 10.12312;
    std::cout << "num2 = " << num2 << '\n';

    //Character example
    char letter = 'a';
    std::cout << "letter = " << letter << '\n';

}

